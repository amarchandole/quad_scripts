#!/bin/bash
cd /home/quad/btrfs-progs/
dmesg -C
echo "_________________________________________________________________________________________________________"
echo "														"
echo " dd : filename = $3, blocksize = $1, count = $2, datatype = urandom						"
echo "_________________________________________________________________________________________________________"
	
dd if=/dev/urandom of=/media/quad/mybtrfs/$3 bs=$1 count=$2 
sleep 1
dmesg -C
sleep 1
echo "_________________________________________________________________________________________________________"
echo "|												   	|"
echo "| sync									Sync i/o with disk	   	|"
sync
sleep 1
echo "|_______________________________________________________________________________________________________|"
echo ""